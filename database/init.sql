CREATE TYPE natural_occurrence AS ENUM ('primordial', 'synthetic', 'from decay');

CREATE TABLE element (
    symbol VARCHAR(2) PRIMARY KEY,
    name VARCHAR(50) NOT NULL UNIQUE,
    atomic_number SMALLINT NOT NULL UNIQUE,
    atomic_weight NUMERIC(9, 5) NOT NULL,
    "group" SMALLINT,
    period SMALLINT NOT NULL,
    block BLOCK NOT NULL,
    phase PHASE NOT NULL,
    natural_occurrence NATURAL_OCCURRENCE NOT NULL
);

